# 1. Nhập hai số từ bàn phím
# Lưu ý: Cần dùng int() để chuyển chuỗi nhập vào thành số nguyên
a = int(input("Nhập số thứ nhất: "))
b = int(input("Nhập số thứ hai: "))

# 2. Tính toán và in kết quả trực tiếp
print("Tổng là:", a + b)
print("Hiệu là:", a - b)
print("Tích là:", a * b)
print("Thương là:", a / b)